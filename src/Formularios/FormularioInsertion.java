     private String ImprimirArreglo(int[] arreglo){
        String numeros = "";
        if(arreglo.length != 0){
            for (int i = 0; i< arreglo.length;i++){
                numeros += arreglo[i] + ", ";
            }
        }
        return numeros;
    }
     
    public void ordenarArreglo(int tama単o, int minimo, int maximo){
        arreglo = new int[tama単o];
            int random;
            for (int i = 0; i < arreglo.length; i++)
            {
               random  = ThreadLocalRandom.current().nextInt(minimo,maximo + 1);
               arreglo[i] = random;
            }
            lblArreglo.setText(ImprimirArreglo(arreglo));
            
            long start = System.nanoTime();
            
            int n = arreglo.length;
            for (int i = 1; i < n; ++i)
            {
                int key = arreglo[i];
                int j = i - 1;

                while (j >= 0 && arreglo[j] > key)
                {
                    comparaciones++;
                    arreglo[j + 1] = arreglo[j];
                    j = j - 1;
                }
                arreglo[j + 1] = key;
                intercambios++;
            }
            long aaa;
            aaa = System.nanoTime();
            double tiempo = (aaa - start)/1000000.0;
            lblTiempo.setText(tiempo + "");
            lblIntercambios.setText(intercambios + "");
            lblComparaciones.setText(comparaciones + "");
            lblArregloOrdenado.setText(ImprimirArreglo(arreglo));
            
        
    }
